function deriveName(filename) {
  const base = filename.replace(/\.svg$/i, '')
  return base
    .replace(/[-_]/g, ' ')
    .split(' ')
    .map(w => {
      const lower = w.toLowerCase()
      if (ACRONYMS[lower]) return ACRONYMS[lower]
      return w.charAt(0).toUpperCase() + w.slice(1).toLowerCase()
    })
    .join(' ')
}

function sanitizeId(name) {
  return name
    .replace(/[^a-zA-Z0-9._-]/g, '-')
    .replace(/-{2,}/g, '-')
    .replace(/^-|-$/g, '')
}

function scanDir(dir, category) {
  if (!fs.existsSync(dir)) return []

  const entries = fs.readdirSync(dir, { withFileTypes: true })
  const icons = []

  for (const entry of entries) {
    const fullPath = path.join(dir, entry.name)
    if (entry.isDirectory()) {
      const subIcons = scanDir(fullPath, category)
      icons.push(...subIcons)
    } else if (entry.name.toLowerCase().endsWith('.svg')) {
      const name = deriveName(entry.name)
      const id = sanitizeId(category + '-' + entry.name.replace(/\.svg$/i, ''))
      const relPath = '/icons/' + path.relative(ICONS_DIR, fullPath).replace(/\\/g, '/')
      icons.push({ id, name, category, url: relPath })
    }
  }

  return icons
}

function main() {
  if (!fs.existsSync(ICONS_DIR)) {
    console.error('Icons directory not found: ' + ICONS_DIR)
    process.exit(1)
  }

  const categories = fs.readdirSync(ICONS_DIR, { withFileTypes: true })
    .filter(d => d.isDirectory())
    .map(d => d.name)

  let allIcons = []
  for (const cat of categories) {
    const catDir = path.join(ICONS_DIR, cat)
    const icons = scanDir(catDir, cat)
    allIcons.push(...icons)
    console.log('  ' + cat + ': ' + icons.length + ' icons')
  }

  allIcons.sort((a, b) => a.category.localeCompare(b.category) || a.name.localeCompare(b.name))

  const categoryList = ['all', ...categories.sort()]

  const output = [
    '// Auto-generated by scripts/build-icon-manifest.js',
    '// Do not edit manually — run `node scripts/build-icon-manifest.js` to regenerate',
    '// Generated at: ' + new Date().toISOString(),
    '',
    "import type { IconDef } from './icons'",
    '',
    'export const ICON_CATEGORIES = ' + JSON.stringify(categoryList.map(c => ({ key: c, label: c })), null, 2) + ' as const',
    '',
    'export const ICON_MANIFEST: IconDef[] = ' + JSON.stringify(allIcons, null, 2),
    '',
  ].join('\n')

  fs.writeFileSync(OUTPUT_FILE, output)
  console.log('\nGenerated ' + OUTPUT_FILE + ' with ' + allIcons.length + ' icons across ' + categories.length + ' categories')
}

main()
