#!/usr/bin/env node
/**
 * build-icon-manifest.js
 * Scans public/icons/ for SVG files and generates src/data/icon-manifest.ts
 *
 * Run: node scripts/build-icon-manifest.js
 */

const fs = require('fs')
const path = require('path')

const ROOT = path.join(__dirname, '..')
const ICONS_DIR = path.join(ROOT, 'public', 'icons')
const OUTPUT_FILE = path.join(ROOT, 'src', 'data', 'icon-manifest.ts')

